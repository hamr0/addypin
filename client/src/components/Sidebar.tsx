import { useState } from "react";
import { useMutation } from "@tanstack/react-query";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { useToast } from "@/hooks/use-toast";
import { apiRequest } from "@/lib/queryClient";
import { useStats } from "@/hooks/useStats";
import { useAuth } from "@/hooks/useAuth";
import { Link } from "wouter";
import { Lock, Unlock } from "lucide-react";
import AddyPinIcon from "./AddyPinIcon";
import { SignInButton } from '@clerk/clerk-react';
import AuthHeader from "./AuthHeader";

interface SidebarProps {
  coordinates: { lat: number; lng: number } | null;
  generatedLink: { webLink: string; emailLink: string } | null;
  onLinkGenerated: (link: { webLink: string; emailLink: string }) => void;
  isMapWidth?: boolean;
}

export default function Sidebar({ coordinates, generatedLink, onLinkGenerated, isMapWidth = false }: SidebarProps) {
  const [email, setEmail] = useState("");
  const [otpCode, setOtpCode] = useState("");
  const [editToken, setEditToken] = useState("");
  const [showOtpInput, setShowOtpInput] = useState(false);
  const [hasGeneratedWithoutEmail, setHasGeneratedWithoutEmail] = useState(false);
  const { toast } = useToast();
  const { data: stats, isLoading: statsLoading } = useStats();

  const generatePinMutation = useMutation({
    mutationFn: async () => {
      if (!coordinates) throw new Error("No coordinates set");
      
      const response = await apiRequest("POST", "/api/pins", {
        latitude: coordinates.lat.toString(),
        longitude: coordinates.lng.toString(),
        shortcode: "", // Will be generated by server
        createdBy: email || undefined, // Optional email
      });
      return response.json();
    },
    onSuccess: (data) => {
      onLinkGenerated({
        webLink: data.webLink,
        emailLink: data.emailLink,
      });
      
      // Check if pin was created without email and disable email input
      if (!email) {
        setHasGeneratedWithoutEmail(true);
      }
      
      // Trigger pin list refresh if user is authenticated and has matching email
      if (data.createdBy) {
        window.dispatchEvent(new CustomEvent('pinCreated', { 
          detail: { createdBy: data.createdBy } 
        }));
      }
      toast({
        title: "addypin generated!",
        description: "Your short link is ready to share.",
      });
    },
    onError: async (error) => {
      // Check if it's a PIN_LIMIT_EXCEEDED error
      if (error instanceof Error && error.message.includes("PIN_LIMIT_EXCEEDED")) {
        toast({
          title: "🚫 addypin limit reached",
          description: "You have reached the 5 addypin limit for this email address. To create new addypins, please delete some existing ones from your collection.",
          variant: "destructive",
          duration: 8000,
        });
      } else if (error instanceof Error && error.message.includes("Too many pins created")) {
        toast({
          title: "⏰ Rate limit exceeded",
          description: "You're creating addypins too quickly! Please wait a moment before creating another one.",
          variant: "destructive",
          duration: 6000,
        });
      } else {
        toast({
          title: "❌ Creation failed", 
          description: error instanceof Error ? error.message : "Failed to generate addypin. Please try again.",
          variant: "destructive",
          duration: 5000,
        });
      }
    },
  });

  const sendOtpMutation = useMutation({
    mutationFn: async (email: string) => {
      const response = await apiRequest("POST", "/api/otp/send", { email });
      return response.json();
    },
    onSuccess: (data, email) => {
      setShowOtpInput(true);
      
      // Check message to determine if email was sent successfully
      if (data.message.includes('sent to your email')) {
        // Actual email sent
        toast({
          title: "Verification Code Sent",
          description: "Check your email for the 6-digit verification code",
          duration: 5000,
        });
      } else {
        // Console fallback mode
        toast({
          title: "Verification Code Sent",
          description: data.message,
          duration: 8000,
        });
        console.log(`\n🔑 ===============================`);
        console.log(`🔑 CHECK SERVER CONSOLE ABOVE FOR OTP CODE`);
        console.log(`🔑 EMAIL: ${email.toUpperCase()}`);
        console.log(`🔑 ===============================\n`);
      }
    },
    onError: (error) => {
      toast({
        title: "Error",
        description: error instanceof Error ? error.message : "Failed to send OTP",
        variant: "destructive",
      });
    },
  });

  const verifyOtpMutation = useMutation({
    mutationFn: async ({ email, code }: { email: string; code: string }) => {
      const response = await apiRequest("POST", "/api/otp/verify", { email, code });
      return response.json();
    },
    onSuccess: (data) => {
      setEditToken(data.editToken);
      toast({
        title: "Verified",
        description: "You can now edit addypin coordinates",
      });
    },
    onError: (error) => {
      toast({
        title: "Error",
        description: error instanceof Error ? error.message : "Invalid OTP",
        variant: "destructive",
      });
    },
  });

  const sendEmailMutation = useMutation({
    mutationFn: async () => {
      if (!coordinates || !email) throw new Error("Missing coordinates or email");
      
      const response = await apiRequest("POST", "/api/send-location", {
        email,
        latitude: coordinates.lat.toString(),
        longitude: coordinates.lng.toString(),
        shortcode: generatedLink ? generatedLink.webLink.split('.')[0] : undefined,
      });
      return response.json();
    },
    onSuccess: () => {
      toast({
        title: "Email Sent!",
        description: "Location details have been sent to your email.",
      });
      setEmail("");
    },
    onError: (error) => {
      toast({
        title: "Error",
        description: error instanceof Error ? error.message : "Failed to send email",
        variant: "destructive",
      });
    },
  });

  const copyToClipboard = async (text: string) => {
    try {
      await navigator.clipboard.writeText(text);
      toast({
        title: "Copied!",
        description: "Link copied to clipboard.",
      });
    } catch (err) {
      toast({
        title: "Error",
        description: "Failed to copy to clipboard.",
        variant: "destructive",
      });
    }
  };

  return (
    <div className="lg:col-span-1 space-y-4">
      {/* Short Link Generator */}
      <div className="bg-white rounded-lg shadow-sm border border-gray-200 p-4">
        <h2 className="text-sm font-semibold text-addypin-dark mb-3 flex items-center">
          <AddyPinIcon className="text-addypin-cyan mr-2" size={16} />
          Generate addypin!
        </h2>

        <div className="space-y-3">
          {/* Honeypot field - hidden from users */}
          <input
            type="text"
            name="website"
            style={{ display: 'none' }}
            tabIndex={-1}
            autoComplete="off"
            aria-hidden="true"
          />
          
          <div>
            <Label htmlFor="creator-email" className="block text-xs font-medium text-addypin-dark mb-1">
              Associate with email (optional)
            </Label>
            <Input
              id="creator-email"
              type="email"
              placeholder={hasGeneratedWithoutEmail ? "Refresh page to create another pin" : "your@email.com"}
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              disabled={hasGeneratedWithoutEmail}
              className={`h-8 ${hasGeneratedWithoutEmail 
                ? 'bg-gray-100 text-gray-400 cursor-not-allowed border-gray-200' 
                : 'border-gray-300 focus:border-addypin-cyan focus:ring-addypin-cyan placeholder:text-gray-400'
              }`}
              data-testid="input-creator-email"
            />
            <p className={`text-xs mt-1 ${hasGeneratedWithoutEmail ? 'text-gray-400' : 'text-gray-500'}`}>
              {hasGeneratedWithoutEmail 
                ? "Pin created! Refresh page to create another pin." 
                : "Without email, pin will be deleted after 72 hours"
              }
            </p>
          </div>

          <Button
            onClick={() => generatePinMutation.mutate()}
            disabled={!coordinates || generatePinMutation.isPending || hasGeneratedWithoutEmail}
            className={`w-full font-semibold py-2 px-4 rounded-lg transition-all duration-200 shadow-lg h-8 ${
              hasGeneratedWithoutEmail 
                ? 'bg-gray-400 cursor-not-allowed text-gray-200' 
                : 'bg-addypin-cyan hover:bg-cyan-600 text-white transform hover:scale-105 active:scale-95 hover:shadow-xl'
            }`}
            data-testid="button-generate"
          >
            <AddyPinIcon className="mr-2" size={14} />
            {generatePinMutation.isPending ? "Generating..." : "Generate!"}
          </Button>
        </div>

        {generatedLink && (
          <div className={`mt-3 space-y-2 p-3 rounded-lg ${email ? 'bg-green-50 border border-green-200' : 'bg-gray-50 border border-gray-200'}`}>
            <h3 className={`text-sm font-semibold mb-3 ${email ? 'text-green-800' : 'text-gray-600'}`}>
              <i className="fas fa-check-circle mr-2"></i>
              {email ? 'Your addypin is ready!' : 'Temporary addypin (expires in 72 hours)'}
            </h3>
            
            <div>
              <Label className={`block text-xs font-medium mb-1 ${email ? 'text-green-700' : 'text-gray-500'}`}>
                addypin link
              </Label>
              <div className={`flex items-center rounded-lg ${email ? 'bg-white border border-green-200 p-3' : 'bg-gray-100 border border-gray-300 p-2'}`}>
                <div className={`flex-1 text-sm font-mono ${email ? 'text-addypin-dark' : 'text-gray-400'} ${!email ? 'cursor-not-allowed' : ''}`}>
                  {generatedLink.webLink || 'Loading...'}
                </div>
                <Button
                  variant="ghost"
                  size="sm"
                  onClick={() => copyToClipboard(generatedLink.webLink)}
                  className={`ml-2 h-auto p-1 ${email ? 'text-addypin-cyan hover:text-cyan-600' : 'text-gray-400 hover:text-gray-500'}`}
                  data-testid="button-copy-web-link"
                >
                  <i className="fas fa-copy"></i>
                </Button>
              </div>
            </div>

            <div>
              <Label className={`block text-xs font-medium mb-1 ${email ? 'text-green-700' : 'text-gray-500'}`}>
                addypin email
              </Label>
              <div className={`flex items-center rounded-lg ${email ? 'bg-white border border-green-200 p-3' : 'bg-gray-100 border border-gray-300 p-2'}`}>
                <div className={`flex-1 text-sm font-mono ${email ? 'text-addypin-dark' : 'text-gray-400'} ${!email ? 'cursor-not-allowed' : ''}`}>
                  {generatedLink.emailLink || 'Loading...'}
                </div>
                <Button
                  variant="ghost"
                  size="sm"
                  onClick={() => copyToClipboard(generatedLink.emailLink)}
                  className={`ml-2 h-auto p-1 ${email ? 'text-addypin-cyan hover:text-cyan-600' : 'text-gray-400 hover:text-gray-500'}`}
                  data-testid="button-copy-email-link"
                >
                  <i className="fas fa-copy"></i>
                </Button>
              </div>
            </div>
          </div>
        )}
      </div>





      {/* Coordinate Editing Section */}
      {generatedLink && email && (
        <div className="bg-white rounded-xl shadow-sm border border-gray-200 p-6" data-testid="edit-coordinates-section">
          <h3 className="text-lg font-semibold text-addypin-dark mb-4 flex items-center">
            <Unlock className="w-5 h-5 text-addypin-cyan mr-3" />
            Edit Coordinates
          </h3>

          <div className="space-y-4">
            <p className="text-sm text-gray-600">
              To edit pin coordinates, we'll send you a verification code.
            </p>
            
            <div>
              <Label htmlFor="edit-email" className="block text-sm font-medium text-addypin-dark mb-2">
                Email address
              </Label>
              <div className="flex space-x-2">
                <Input
                  id="edit-email"
                  type="email"
                  placeholder="your@email.com"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  className="flex-1 border-gray-300 focus:border-addypin-cyan focus:ring-addypin-cyan"
                  data-testid="input-edit-email"
                />
                <Button
                  onClick={() => sendOtpMutation.mutate(email)}
                  disabled={!email || sendOtpMutation.isPending}
                  className="bg-addypin-cyan hover:bg-cyan-600 text-white px-4"
                  data-testid="button-send-otp"
                >
                  {sendOtpMutation.isPending ? "Sending..." : "Send OTP"}
                </Button>
              </div>
            </div>

            {showOtpInput && (
              <div>
                <Label htmlFor="otp-code" className="block text-sm font-medium text-addypin-dark mb-2">
                  Verification Code
                </Label>
                <div className="flex space-x-2">
                  <Input
                    id="otp-code"
                    type="text"
                    placeholder="123456"
                    value={otpCode}
                    onChange={(e) => setOtpCode(e.target.value.replace(/\D/g, '').slice(0, 6))}
                    maxLength={6}
                    className="flex-1 border-gray-300 focus:border-addypin-cyan focus:ring-addypin-cyan font-mono text-center text-lg"
                    data-testid="input-otp-code"
                  />
                  <Button
                    onClick={() => verifyOtpMutation.mutate({ email, code: otpCode })}
                    disabled={!otpCode || verifyOtpMutation.isPending}
                    className="bg-green-600 hover:bg-green-700 text-white px-4"
                    data-testid="button-verify-otp"
                  >
                    {verifyOtpMutation.isPending ? "Verifying..." : "Verify"}
                  </Button>
                </div>
                <div className="text-xs text-orange-600 bg-orange-50 p-3 rounded border-l-4 border-orange-400">
                  <div className="font-semibold mb-2">🔍 Development Mode - Find Your OTP:</div>
                  <div className="space-y-1">
                    <div><strong>Step 1:</strong> Look at the <strong>Workflow Console</strong> (server logs)</div>
                    <div><strong>Step 2:</strong> Find the box with "🔑 OTP CODE FOR YOUR_EMAIL"</div>
                    <div><strong>Step 3:</strong> Copy the 6-digit number and paste it above</div>
                  </div>
                </div>
              </div>
            )}

            {editToken && (
              <div className="p-3 bg-green-50 border border-green-200 rounded-lg">
                <p className="text-sm text-green-700 flex items-center">
                  <Unlock className="w-4 h-4 mr-2" />
                  Verified! You can now edit coordinates by clicking on the map.
                </p>
              </div>
            )}
          </div>
        </div>
      )}

      {/* Footer */}
      <div className="text-center mt-6">
        {/* Empty footer space - Version History moved to Home page Resources */}
      </div>
    </div>
  );
}
